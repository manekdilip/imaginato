extension UIDevice {
    enum DeviceTypes {
        case iPad
        case iPadPro
        case iPhone4_4s
        case iPhone5_5s
        case iPhone6_6s
        case iPhone6p_6ps
        case iPhoneX_XS
        case iPhoneXSMax
        case iPhoneXR
    }
    
    static var deviceType : DeviceTypes {
        if UIDevice().userInterfaceIdiom == .phone {
            switch UIScreen.main.nativeBounds.height {
            case 1136:
                //print("iPhone 5 or 5S or 5C")
                return .iPhone5_5s
            case 1334:
               // print("iPhone 6/6S/7/8")
                return .iPhone6_6s
            case 1920, 2208:
               // print("iPhone 6+/6S+/7+/8+")
                return .iPhone6p_6ps
            case 2436:
               // print("iPhone X/XS/11 Pro")
                return .iPhoneX_XS
            case 2688:
               // print("iPhone XS Max/11 Pro Max")
                return .iPhoneXSMax
            case 1792:
               // print("iPhone XR/ 11 ")
                return .iPhoneXSMax
            default:
              //  print("Unknown")
                return .iPhone4_4s
            }
        }else{
            switch UIScreen.main.bounds.height{
            case 480.0:
                return .iPhone4_4s
            case 568.0:
                return .iPhone5_5s
            case 667.0:
                return .iPhone6_6s
            case 736.0:
                return .iPhone6p_6ps
            case 812.0:
                return .iPhoneX_XS
            case 1024.0:
                return .iPad
            case 1366.0:
                return .iPadPro
            default:
                //for 896
                return .iPhoneXSMax
            }
        }
    }
}
